package Exemplos;
public class DoubleType {
    public static void main(String[] args) {
        double n1 = 19.99d;
        double numeroDouble = n1 * 2;           // (numeroDouble) para calculo com variavel com resultado mais preciso
        System.out.println(numeroDouble / 3);
        
        System.out.println(n1);  
    }
}
