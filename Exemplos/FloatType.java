package Exemplos;
public class FloatType {
    public static void main(String[] args) {
        float n1 = 5.75f;   
        System.out.println(n1);  

        float n2 = 19.99f;
        float numeroFloat = n2 * 2;               // (numeroFloat) para calculo com variavel com resultado menos preciso que o 'double'
        System.out.println(numeroFloat / 3);
    }
}
