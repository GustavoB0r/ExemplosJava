package Exemplos;
public class DiferentDataTypes {
    public static void main(String[] args) {
        int n1 = 5;                       // integer ou int (numero inteiro)
        float numeroFloat = 5.99f;        // float (numero com virgula)
        char letra = 'D';                 // character
        boolean verdaFalso = true;        // boolean (verdadeiro ou falso)
        String text = "Hello";            // String (frase)
        System.out.println(n1);
        System.out.println(numeroFloat);
        System.out.println(letra);
        System.out.println(verdaFalso);
        System.out.println(text);
    }
}
