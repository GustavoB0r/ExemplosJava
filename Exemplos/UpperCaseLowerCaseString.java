package Exemplos;
public class UpperCaseLowerCaseString {
    public static void main(String[] args) {
        String txt = "Ola Mundo";
        System.out.println(txt.toUpperCase());          //imprimi a string tudo em letras maiusculas 
        System.out.println(txt.toLowerCase());          //imprimi a string tudo em letras minusculas
    }
}
