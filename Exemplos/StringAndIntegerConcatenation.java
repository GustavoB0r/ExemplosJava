package Exemplos;
public class StringAndIntegerConcatenation {
    public static void main(String[] args) {
        String x = "10";
        int y = 20;
        String z = x + y;
        System.out.println(z);        //imprimi as duas variaveis, como uma nao é (int), ela é considerada palavra, então o resultado sera colocado o 10 ao lado de 20 = 1020
    }
}
